<h1>2005 - 1º Colóquio de Moda</h1>
<h2>Ribeirão Preto/Sp</h2>
<h3>14 a 16 de setembro de 2005</h3>
<p>Sediado pelo Centro Universitário Moura Lacerda- Ribeirão Preto.
    <br>O primeiro Colóquio Nacional de Moda foi criado e sediado no Centro Universitário Moura Lacerda, por meio da Profa. Dra. Maria de Fatima Costa Mattos (coordenadora do curso à época) e pela Profa. Dra. Kathia Castilho Cunha, à convite, na cidade de Ribeirão Preto (SP).
    <br>Contou com a participação de 16 instituições de ensino e pesquisa, integrando pesquisadores de nove estados brasileiros. Nos três dias de encontro (manhã e tarde), foram apresentados 77 trabalhos selecionados por um Comitê Científico interestadual.
</p>
