<h1>2008 – 4º Colóquio de Moda
<br>1ª edição internacional</h1>
<h2>Novo Hamburgo/RS</h2>
<h3>29 de setembro a 02 de outubro de 2008</h3>

<p>Esta edição do Colóquio de Moda foi sediada na FEEVALE em novo Hamburgo. Contou com a participação de 40 instituições de ensino superior em moda no Brasil. Foram apresentados mais de 200 trabalhos selecionados com um número de inscritos superior a 1000 participantes. Contou coma a presença de palestrantes da área de calçado e malharia, setores referência da região Sul.</p>
<p>Com participação de Palestrantes Internacionais bem como de público inscrito de outras nacionalidades, tratou-se da 1ª Edição Internacional do evento.</p>
<p>Representantes das Instituições de Ensino Superior em Moda do Rio Grande do Sul como a UCS, UFRGS, UniRitter e UCPEL, compuseram, à convite do curso de Design de Moda e Tecnologia da FEEVALE, a Comissão Local, proporcionando, assim, a integração das escolas da região.</p>
