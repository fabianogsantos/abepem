<h1>2013 – 9º Colóquio de Moda
<br><small>6ª edição internacional</small></h1>
<h2>Fortaleza (CE)</h2>

<p align='justify'>Nesse ano o Colóquio foi realizado na cidade de Fortaleza, na Universidade Federal do Ceará, no período de 9 a 12 de setembro. Aberto a acadêmicos (pesquisadores, professores e alunos) e profissionais de Moda, bem como da área do Design, Artes Plásticas, Arquitetura, Comunicação, História, Figurino, Psicologia, Sociologia, Antropologia, Marketing, Jornalismo, Administração e áreas afins.</p>
<p align='justify'>Sendo um congresso inter e transdisciplinar, o Colóquio procura explorar a Moda do ponto de vista social, cultural, psicológico, e comunicacional, buscando reunir diferentes abordagens e pontos de vista sobre a prática, o ensino e a cultura da Moda.</p>
<p align='justify'>O 9º Colóquio de Moda ofereceu 8 grandes eixos temáticos: Design; Educação; Cultura (história, sociologia,etc.); Comunicação; Marketing (consumo, marca, negócios);  Processos Produtivos; Figurino e Sustentabilidade.</p>
