    <h3>O QUE É UM PÔSTER?</h3>
    <p>É uma forma de comunicação de um trabalho de pesquisa, concluído ou em andamento, feito normalmente por graduandos sob a orientação de um professor orientador.
    <br>
    O graduado ou pós-graduado (especialista, mestre ou doutor), quando em pesquisas iniciais, também podem apresentar os seus resultados sob forma de pôster.
    <br>
    O trabalho poderá ter no máximo 3 autores.</p>

    <h3>Como posso participar com um Pôster?</h3>
    <aprovação.>A inscrição de um trabalho sob forma de pôster visa à divulgação de trabalhos em andamento ou concluídos, de graduandos ou graduados. Neste caso, necessariamente, devem ser trabalhos de TCC, Pesquisa Artística, Iniciação Científica, Iniciação Tecnológica ou de Inovação, desenvolvidos por professores e alunos dos cursos superiores de graduação (bacharelado ou superior de tecnologia) ou pós-graduação em Moda ou áreas afins.
    <br>No caso do participante graduando, ele deverá estar cursando ainda o último ano do curso, e ter desenvolvido o seu trabalho sob a orientação de um professor.
    <br>A comprovação da regularidade de matrícula do aluno deverá ser enviada juntamente com o artigo para aprovação.</p>
@endsection
